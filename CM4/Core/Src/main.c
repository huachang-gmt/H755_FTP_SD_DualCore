/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

/* USER CODE BEGIN Boot_Mode_Sequence_1 */
#if defined(DUAL_CORE_BOOT_SYNC_SEQUENCE)
  /*HW semaphore Clock enable*/
  __HAL_RCC_HSEM_CLK_ENABLE();
  /* Activate HSEM notification for Cortex-M4*/
  HAL_HSEM_ActivateNotification(__HAL_HSEM_SEMID_TO_MASK(HSEM_ID_0));
  /*
  Domain D2 goes to STOP mode (Cortex-M4 in deep-sleep) waiting for Cortex-M7 to
  perform system initialization (system clock config, external memory configuration.. )
  */
  HAL_PWREx_ClearPendingEvent();
  HAL_PWREx_EnterSTOPMode(PWR_MAINREGULATOR_ON, PWR_STOPENTRY_WFE, PWR_D2_DOMAIN);
  /* Clear HSEM flag */
  __HAL_HSEM_CLEAR_FLAG(__HAL_HSEM_SEMID_TO_MASK(HSEM_ID_0));

#endif /* DUAL_CORE_BOOT_SYNC_SEQUENCE */
/* USER CODE END Boot_Mode_Sequence_1 */
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  __HAL_RCC_D2SRAM1_CLK_ENABLE();
  __HAL_RCC_D2SRAM2_CLK_ENABLE();
  __HAL_RCC_D2SRAM3_CLK_ENABLE();

  /* USER CODE END Init */

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_LWIP_Init();
  tcp_server_init();
  /* USER CODE BEGIN 2 */

  uint32_t last_link_check = 0;
  uint32_t toggle_timer = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // 1. 維持 LwIP 運行，處理收發封包 (這是最重要的一行！)
    MX_LWIP_Process();

    // 2. 每 200ms 檢查一次實體網路線狀態
    if (HAL_GetTick() - last_link_check > 200) 
    {
        last_link_check = HAL_GetTick();
        
        // 更新並檢查底層 PHY 狀態 (依據 CubeMX 版本，ethernet_link_check_state 宣告在 ethernetif.h 中)
        ethernet_link_check_state(&gnetif); 

    }

    // 3. 系統存活指示燈，每 500ms 閃爍一次黃燈
    // 這可以證明 CM4 核心正常運作，且沒有因為 Memory 或 DMA 設定錯誤而卡死
    if (HAL_GetTick() - toggle_timer > 500) 
    {
        toggle_timer = HAL_GetTick();
        //HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_1); // 翻轉黃燈 (PE1)
    }
  }
  /* USER CODE END 3 */
}

